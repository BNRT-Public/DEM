    SUBROUTINE SIZEPOLO(M,N,L,LCO,LCX,LCY,LCZ,NP)
    IMPLICIT NONE
    !
    ! DATA:  16/09/2019
    ! AUTOR: Boris N. Rojo Tanzi
    !
    ! Calcula tamano de polos para aleatoriedad
    !
    !--------------------------------------------------------------------------
    ! VARIABLES DE ENTRADA SALIDA
    !--------------------------------------------------------------------------
    INTEGER M,N,L
    DOUBLE PRECISION LCO, LCX, LCY, LCZ
    INTEGER NP(4)
    
    !--------------------------------------------------------------------------
    ! VARIABLES LOCALES
    !--------------------------------------------------------------------------
    DOUBLE PRECISION D
    
    !--------------------------------------------------------------------------
    D=DSQRT(((LCO*M)**2.0)+((LCO*N)**2.0)+((LCO*L)**2.0))
    D=D+(2.0*LCO)
    
    NP(1)=INT(D/LCX)+2 ! X
    NP(2)=INT(D/LCY)+2 ! Y
    NP(3)=INT(D/LCZ)+2 ! Z

    NP(4)=NP(1)*NP(2)*NP(3) ! TOTAL

    RETURN
    END
