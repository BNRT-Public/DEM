    REAL FUNCTION GAMA(ARG)
    !
    ! DATA:  02/07/2019
    ! AUTOR: Ignacio Iturrioz
    !        Boris N. Rojo Tanzi
    !
    ! ESTA FUNCION CALCULA LA FUNCION GAMMA
    !
    !--------------------------------------------------------------------------
    INTEGER N,FACT,I
    DOUBLE PRECISION ARG,P,K,ARGG
    !--------------------------------------------------------------------------
    K=ARG*(ARG+1.0D00)*(ARG+2.0D00)*(ARG+3.0D00)
    ARGG=ARG
    ARGG=ARGG+4.0D00
    N=INT(ARGG)
    P=ARGG-N

    FACT=1
    DO I=1,N
        FACT=FACT*I
    END DO

    GAMA=FACT*(((N+P/2)**2+(2-P)*P/12)**((P-1)/2))/K

    RETURN
    END
