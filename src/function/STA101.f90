    FUNCTION STA101 (P,UUF)
    !     ===============
    !--------------------------------------------------------------------------
    !  IFM UNIV IBK RS  02.SEP.1986
    !  UPDATE       MMR 17.AUG.1990
    !--------------------------------------------------------------------------
    !  PURPOSE:
    !  INVERSE CUMULATIVE GAUSSIAN DISTRIBUTION (STANDARDIZED).
    !--------------------------------------------------------------------------
    !  PARAMETERS:
    !  P    PROBABILITY. (UNCHANGED ON EXIT)
    !  UUF  MINIMUN DOUBLE PRECISION NUMBER OF THE MACHINE. (UNCHANGED ON EXIT)
    !--------------------------------------------------------------------------
    !  COMMON BLOCKS:
    !  NONE
    !--------------------------------------------------------------------------
    !  SUBROUTINES AND FUNCTIONS CALLED:
    !  NONE
    !--------------------------------------------------------------------------
    !  DEVIATIONS FROM STANDARD FORTRAN F77:
    !  NONE
    !--------------------------------------------------------------------------
    IMPLICIT DOUBLE PRECISION (A-H,O-Z)
    DATA     ZERO,ONE,HALF  /0.0D00,1.0D00,0.5D00/
    DATA     P0,P1,P2,P3,P4                                           &
        &         /-.322232431088D00,-.100000000000D+01,-.3422422088547D00,&
        &         -.204231210245D-01,-.453642210148D-04/
    DATA     Q0,Q1,Q2,Q3,Q4                                           &
        &         /.993484626060D-01, .588581570495D00,                    &
        &          .531103462366D00, .103537752850D00, .3856070063400D-02/
    !--------------------------------------------------------------------------
    ALIMIT  = DSQRT(UUF)
    
    X = ZERO
    PS = P
    
    IF(PS.GT.  HALF) PS = ONE-PS
    IF(PS.LT.ALIMIT) PS = ALIMIT
    IF(PS.EQ.  HALF) RETURN
    !
    Y = DSQRT(DLOG(ONE/(PS*PS)))
    X = Y+((((Y*P4+P3)*Y+P2)*Y+P1)*Y+P0)/((((Y*Q4+Q3)*Y+Q2)*Y+Q1)*Y+Q0)
    
    IF(P.LT.HALF) X = -X
    
    STA101 = X
    
    RETURN
    END
